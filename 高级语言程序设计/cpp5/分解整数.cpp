void convert(int n)
{
	if (n != 0 && n / 10 == 0)
	{
		if (n > 0)
		{
			cout << char('0' + n % 10) << " ";
		}
		else
		{
			cout << "- " << char('0' - n % 10) << " ";
		}
		
		return;
	}
	else if(n)
	{
		convert(n / 10);
		if (n > 0)
		{
			cout << char('0' + n % 10) << " ";
		}
		else
		{
			cout << char('0' - n % 10) << " ";
		}
	}
	else
	{
		cout << char('0' + n) << " ";
		return;
	}
	//输入超出范围，取上限或下限
}

