	cout << cnt(end_ch) << endl; /* 按字母塔最大宽度输出=(不允许用循环) */
	print_tower(end_ch, num(end_ch), num(end_ch), 1);
	cout << endl;

	/* 倒三角字母塔(两边为A) */
	cout << cnt(end_ch) << endl; /* 按字母塔最大宽度输出=(不允许用循环) */
