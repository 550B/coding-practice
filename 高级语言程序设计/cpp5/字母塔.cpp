   /***************************************************************************
	 函数名称：print_tower
	 功    能：打印字母塔
	 输入参数：char a,int b
	 返 回 值：void
	 说    明：形参按需设置
   ***************************************************************************/
void print_tower(char a, int t, int tt, int judge)
{
	/* 按需实现，函数中不允许任何形式的循环*/
	
	cout << setiosflags(ios::right);
	
	if (judge)
	{
		if (t == 0)
		{
			return;
		}
		else
		{
			print_tower(a - 1, t - 1, tt, 1);
			cout_space1(tt, t);
			abc1(a);
			cout << 'A';
			abc2('A', a);
			cout << endl;
		}
	}
	else
	{
		if (t == tt)
		{
			return;
		}
		else
		{
			print_tower(a - 1, t + 1, tt, 0);
			cout_space2(tt, t);
			abc3('A' + tt - 1, a - 1);
			abc4(a, 'A' + tt);
			cout << endl;
		}
	}
}

