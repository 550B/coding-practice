   /***************************************************************************
		函数名称：cnt
		功    能：打印最大宽度=
		输入参数：char a
		返 回 值：char
		说    明：形参按需设置
	  ***************************************************************************/
char cnt(char a)
{
	if (a == 'A')
	{
		return '=';
	}
	cout << "==";
	return cnt(a - 1);
}

/***************************************************************************
		函数名称：num
		功    能：返回层数
		输入参数：char a
		返 回 值：int
		说    明：形参按需设置
	  ***************************************************************************/
int num(char a)
{
	return (int)(a) - 64;
}

/***************************************************************************
		函数名称：cout_space1
		功    能：输出空格
		输入参数：int t, int tt
		返 回 值：void
		说    明：形参按需设置
	  ***************************************************************************/
void cout_space1(int t, int tt)
{
	if (t - tt == 0)
	{
		return;
	}
	else
	{
		cout << " ";
		cout_space1(t - 1, tt);
	}
}

/***************************************************************************
		函数名称：cout_space2
		功    能：输出空格
		输入参数：int t, int tt
		返 回 值：void
		说    明：形参按需设置
	  ***************************************************************************/
void cout_space2(int t, int tt)
{
	if (t - 1 == tt)
	{
		return;
	}
	else
	{
		cout_space2(t, tt + 1);
		cout << " ";
		
	}
}

